package org.wso2.healthcare.consentmgt.core.model;

import java.util.ArrayList;
import java.util.List;

/**
 * This class holds the model structure for default FHIR consent purpose.
 */
public class FhirPurpose {
    private String name;
    private String description;
    private String group;
    private String groupType;
    private boolean mandatory;
    private List<String> piiCategoryNames;

    public FhirPurpose() {
        piiCategoryNames = new ArrayList<>();
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public String getGroupType() {
        return groupType;
    }

    public void setGroupType(String groupType) {
        this.groupType = groupType;
    }

    public boolean isMandatory() {
        return mandatory;
    }

    public void setMandatory(boolean mandatory) {
        this.mandatory = mandatory;
    }

    public List<String> getPiiCategoryNames() {
        return piiCategoryNames;
    }

    public void setPiiCategoryNames(List<String> piiCategoryNames) {
        this.piiCategoryNames = piiCategoryNames;
    }
}
